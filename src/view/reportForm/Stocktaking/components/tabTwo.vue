<script>
import * as api from "_api/reportForm/index.js";
export default {
  data() {
    return {
      page: {
        page: 0,
        size: 10,
        total: 0
      },
      tableDataAll: [],
      tableData: [],
      searchData:{}
    };
  },
  mounted() {
    this.getList();
  },
  methods: {
    // 查询表
    async getList(data = {}) {
      this.searchData = data;
      let res = await api.getStockCheckLoss(data,this.page);
      if (res.code == 0) {
        this.tableDataAll = (res.data.content || []).map(el => {
          if ([1, "1", "是"].includes(el.taxSign)) {
            el.taxSign = true;
          }
          if ([0, "0", "否"].includes(el.taxSign)) {
            el.taxSign = false;
          }
          return el;
        });

        this.tableData = this.tableDataAll;
        this.page.total = res.data.totalElements;
      }
    },
    //分页
    changePage(p) {
      this.page.page = p-1;
      this.getList();
    },
    changeSize(size) {
      this.page.page = 0;
      this.page.size = size;
      this.getList();
    },
    //表尾合计
    footerMethod({ columns, data }) {
      return [
        columns.map((column, columnIndex) => {
          if (columnIndex === 0) {
            return "合计";
          }
          if (
            [
              "orderQty",
              "orderPrice",
              "orderAmt",
              "taxPrice",
              "taxAmt",
              "noTaxPrice",
              "noTaxAmt"
            ].includes(column.property)
          ) {
            return this.$utils.sum(data, column.property);
          }
          return null;
        })
      ];
    },
    //导出
    async exportFun(){
      let pageObj = {
        page:0,
        size:this.page.total
      }
      return new Promise(async (resolve, reject) => {
        let res = await api.getStockCheckLoss(this.searchData,pageObj);
        if (res.code == 0) {
          let arrData = (res.data.content || []).map(el => {
            if ([1, "1", "是"].includes(el.taxSign)) {
              el.taxSign = true;
            }
            if ([0, "0", "否"].includes(el.taxSign)) {
              el.taxSign = false;
            }
            return el;
          });
          resolve(arrData);
        }else{
          reject()
        }
      })
    }
  }
};
