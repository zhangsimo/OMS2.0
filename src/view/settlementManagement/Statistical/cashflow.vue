<script>
import {
  are,
  // goshop
} from "@/api/settlementManagement/fundsManagement/capitalChain";
import { goshop } from '@/api/settlementManagement/shopList';
import * as api from "_api/settlementManagement/financialStatement.js";
import QuickDate from "@/components/getDate/dateget_bill.vue";
import moment from "moment";
import XEUtils from "xe-utils";
export default {
  components: {
    QuickDate
  },
  data() {
    return {
      // 数据类
      headData: {
        balanceMoney: 0,
        incomeMoney: 0,
        paidMoney: 0,
        yesterDayMoney: 0
      },
      tableData: [], // 主表
      dates: [], // 查询日期
      areaId: "", // 区域id
      areas: [], // 区域
      BranchstoreId: "", // 门店id
      Branchstore: [] // 门店
    };
  },
  async mounted() {
    this.dates = this.$refs.quickDate.val;
    this.getArea();
    this.getShop();
    this.query();
  },
  computed:{
    selectShopList(){
      let canSelect = this.$store.state.user.userData.currentCompany.isMaster ? true : false
      return canSelect
    }
  },
  methods: {
    mergeRowMethod({ row, $rowIndex, column, data }) {
      const fields = ["maxterm"];
      const cellValue = XEUtils.get(row, column.property);
      if (cellValue && fields.includes(column.property)) {
        const prevRow = data[$rowIndex - 1];
        let nextRow = data[$rowIndex + 1];
        if (prevRow && XEUtils.get(prevRow, column.property) === cellValue) {
          return { rowspan: 0, colspan: 0 };
        } else {
          let countRowspan = 1;
          while (
            nextRow &&
            XEUtils.get(nextRow, column.property) === cellValue
          ) {
            nextRow = data[++countRowspan + $rowIndex];
          }
          if (countRowspan > 1) {
            return { rowspan: countRowspan, colspan: 1 };
          }
        }
      }
    },
    // 快速查询
    getDataQuick(v) {
      this.dates = v;
      this.query();
    },
    // 区域
    async getArea() {
      let res = await are();
      if (res.code === 0) return (this.areas = [...this.areas, ...res.data]);
    },
    // 门店
    async getThisArea() {
      let data = {};
      data.shopkeeper = 1;
      data.shopNumber = this.$store.state.user.userData.shopId;
      data.tenantId = this.$store.state.user.userData.tenantId;
      let res = await are(data);

      if (res.code === 0) {
        this.areaId = res.data[0].id;
      }
    },

    //切换地址重新调取门店接口
    changeArea() {
      if (this.$store.state.user.userData.shopkeeper == 0) {
        this.shopCode = 0;
        this.getShop();
      }
    },

    //获取门店
    async getShop() {
      let data = {};
      data.supplierTypeSecond = this.BranchstoreId;
      this.Branchstore = [{ id: 0, name: "全部" }];
      let res = await goshop(data);
      if (res.code === 0) {
        this.Branchstore = [...this.Branchstore, ...res.data];
        this.$nextTick(() => {
          this.BranchstoreId = this.$store.state.user.userData.shopId;
        });
        if (this.$store.state.user.userData.shopkeeper != 0) {
          this.getThisArea(); //获取当前门店地址
        }
      }
    },
    // 查询
    async query() {
      let params = {
        areaId: this.areaId,
        shopNumber: this.BranchstoreId,
        size: 10000
      };

      if (this.dates.length === 2 && this.dates[0]) {
        params.startTime =
          moment(this.dates[0]).format("YYYY-MM-DD");
        params.endTime =
          moment(this.dates[1]).format("YYYY-MM-DD");
      }

      params.page = 0;

      for (let key in params) {
        if (!params[key]) {
          Reflect.deleteProperty(params, key);
        }
      }

      let res = await api.findListPageAllCashFlow(params);

      if (res.code == 0) {
        this.tableData = res.data.content;
        this.headData = res.data.content[0]["heandMoney"];
      }
    }
  }
};
</script>

