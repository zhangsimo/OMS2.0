<template>
  <div class="content-oper content-oper-flex" style="background-color: #fff;">
    <section class="oper-box">
      <div class="oper-top flex">
        <div class="wlf">
          <div class="db mr15">
            <span>快速查询：</span>
          </div>
          <div class="db mr15">
            <quick-date
              class="mr10"
              ref="quickDate"
              @quickDate="getDataQuick"
            ></quick-date>
          </div>
          <div class="db ml15">
            <span>起止日期：</span>
            <Date-picker
              v-model="dates"
              type="daterange"
              placeholder="选择日期"
              class="w200"
            ></Date-picker>
          </div>
          <div class="db ml15">
            <span>区域：</span>
            <Select v-model="areaId" class="w150" :disabled="selectShopList">
              <Option v-for="item in areas" :value="item.id" :key="item.id">{{
                item.companyName
              }}</Option>
            </Select>
          </div>
          <div class="db ml15">
            <span>门店：</span>
            <Select v-model="BranchstoreId" class="w150" :disabled="selectShopList">
              <Option
                v-for="item in Branchstore"
                :value="item.id"
                :key="item.id"
                >{{ item.name }}</Option
              >
            </Select>
          </div>
          <div class="db ml15">
            <button
              class="mr10 ivu-btn ivu-btn-default"
              type="button"
              @click="query"
            >
              <i class="iconfont iconchaxunicon"></i>
              <span>查询</span>
            </button>
          </div>
        </div>
      </div>
      <div>
        <div class="head">
          <span>昨日余额</span>
          <span>{{ headData.yesterDayMoney }}</span>
          <span>本日收款</span>
          <span>{{ headData.incomeMoney }}</span>
          <span>本日付款</span>
          <span>{{ headData.paidMoney }}</span>
          <span>本日余额</span>
          <span>{{ headData.balanceMoney }}</span>
        </div>
      </div>
    </section>

    <div class="mt15 warp_table">
      <vxe-table
        border
        resizable
        auto-resize
        align="center"
        height="600"
        :span-method="mergeRowMethod"
        :data="tableData"
      >
        <vxe-table-column title="《现金流量表》">
          <vxe-table-column title="项目信息">
            <vxe-table-column title="大项" field="maxterm"></vxe-table-column>
            <vxe-table-column
              title="现金流科目"
              field="cashAccount"
            ></vxe-table-column>
            <vxe-table-column title="行次" field="rowNo"></vxe-table-column>
            <vxe-table-column
              title="具体项目类别"
              field="accountType"
            ></vxe-table-column>
            <vxe-table-column
              title="凭证对应会计科目"
              field="voucherAccount"
            ></vxe-table-column>
          </vxe-table-column>
          <vxe-table-column title="金额信息">
            <vxe-table-column title="现金" field="cashMoney"></vxe-table-column>
            <vxe-table-column
              title="银行卡"
              field="cartoonMoney"
            ></vxe-table-column>
            <vxe-table-column
              title="银行账户"
              field="accountMoney"
            ></vxe-table-column>
            <vxe-table-column
              title="资金合计"
              field="totalMoney"
            ></vxe-table-column>
          </vxe-table-column>
          <vxe-table-column title="其他信息">
            <vxe-table-column title="备注" field="remarks"></vxe-table-column>
          </vxe-table-column>
        </vxe-table-column>
      </vxe-table>
    </div>
  </div>
