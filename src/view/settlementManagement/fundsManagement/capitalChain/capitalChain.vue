  import {are , goshop , goSubject , impUrl , goList , deleList} from '@/api/settlementManagement/fundsManagement/capitalChain'

  import moment from 'moment'
  export default {
    components: {
      quickDate,
      importXLS,
      artificial
    },
    data() {
      return {
        accountName:'',//账号
        bankName:'',//开户银行
        value: [],
        Branchstore: [
          {id:0 ,companyName:'全部'}
        ],
        model1: 0, //获取到地址id
        shopCode:0,//获取到门店id
        shopList: [
          {id:0 , name:'全部'}
        ], //门店列表
        subjectCode:0,//科目id
        subJectList:[
          {id:0 ,titleName:'全部'}
        ],//科目列表
        company: "", //往来单位
        companyId: "", //往来单位id
        formInline:{},//统计数据
        tableData:[],//全部数据
        tableData1:[],//已审核数据
        tableData2:[],//未审核数据
        impirtUrl:{
          downId: '1200000000',
          upUrl:impUrl
        },//下载上传路径
        oneList:{},//点击获取到的信息
      };
    },
    async mounted () {
      let arr = await creat (this.$refs.quickDate.val,this.$store)
      this.value = arr[0];
      this.getAllAre() //获取区域
      this.getShop()  //获取门店
      this.getSubject()//获取科目
    },
    methods: {

      //获取全部地址
      async getAllAre(){
        let res = await are()
        if (res.code === 0) return this.Branchstore = [...this.Branchstore ,...res.data]
      },

      //切换地址重新调取门店接口
      changeArea(){
        this.shopCode = 0
        this.getShop()

      },

      //获取门店
     async getShop(){
        let data ={}
        data.supplierTypeSecond = this.model1
       let res = await goshop(data)
       if (res.code === 0) return this.shopList = [...this.shopList , ...res.data]
      },

      //获取科目
      async getSubject(){
        let res = await goSubject()
        if(res.code === 0) return this.subJectList = [...this.subJectList , ...res.data]
      },

      // 日期选择
      dateChange(data){
        this.value = data
      },
      // 快速查询
      quickDate(data){
        this.value = data
      },

      //获取表格信息
      async getList(){
        let data = {}
        data.page = 0
        data.size = 9999
        data.startTime = moment(this.value[0]).startOf('day').format("YYYY-MM-DD HH:mm:ss")
        data.endTime = moment(this.value[1]).endOf('day').format("YYYY-MM-DD HH:mm:ss")
        data.areaId = this.model1
        data.shopId = this.shopCode
        data.subjectId = this.subjectCode
        data.accountName = this.accountName
        data.bankName = this.bankName
        let res = await goList(data)
        if(res.code === 0){
          this.tableData = res.data.content
          this.tableData1 = []
          this.tableData2 = []
          res.data.content.forEach( item => {
          if (item.collateState) {
            this.tableData1.push(item)
          } else {
            this.tableData2.push(item)
          }
        })
        }
      },

      //查询
      query(){
        this.getList()
      },

      //点击获取表格数据
      getOneList(val){
        this.oneList = val.row
        console.log(val ,789)
      },

      //打开导入模板下载
      importXSL(){
        this.$refs.imp.openModal()
      },

      //导入成功后刷新页
      getNew(data){},


      //删除导入
      dele(){
        if(Object.keys(this.oneList).length == 0) return this.$Message.error('请至少选择一条数据')
        if(this.oneList.collateState) return this.$Message.error('已审核数据不能删除')
        this.$Modal.confirm({
          title: '提示',
          content: '<p>是否删除该条数据</p>',
          onOk: async () => {
            let data ={}
            data.id = this.oneList.id
            let res = await deleList(data)
            if (res.code === 0){
              this.getList()
              this.$Message.success('删除成功')
            }
          },
          onCancel: () => {
          }
        });
      },

      //智能匹配
      intellect(){
        this.$Modal.success({
          title: '提示',
          content: '删除X条成功'
        });
      },

      //人工匹配
      artificialChange(){
        if(Object.keys(this.oneList).length == 0) return this.$Message.error('请至少选择一条数据')
        this.$refs.art.openModal()

      }

    }
  };
</script>

